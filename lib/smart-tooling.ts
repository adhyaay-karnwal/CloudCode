    '        export PATH="/home/user/.cloudcode/bin:$PATH"',
    '        record_tooling "use bun $(bun --version) source=existing"',
    "      else",
    '        install_mise_tool bun latest "package.json packageManager"',
    "      fi",
    "      ;;",
    "    pnpm|yarn)",
