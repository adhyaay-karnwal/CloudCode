    "    fi",
    '    npm install -g --force "$PACKAGE_SPEC"',
    "    mise reshim || true",
    '    PACKAGE_MANAGER_BIN="$(package_manager_path)"',
    '    if [ -z "$PACKAGE_MANAGER_BIN" ] || ! package_manager_version_matches "$PACKAGE_MANAGER_BIN"; then',
    '      echo "$PACKAGE_NAME is not installed after attempting $PACKAGE_SPEC." >&2',
    "      exit 127",
    "    fi",
    '    "$PACKAGE_MANAGER_BIN" --version >/dev/null',
    '    record_tooling "install $PACKAGE_SPEC source=$PACKAGE_SOURCE"',
    "  else",
    '    record_tooling "defer $PACKAGE_SPEC reason=npm-unavailable"',
    "  fi",
    "}",
