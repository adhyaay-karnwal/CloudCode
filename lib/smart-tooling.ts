    '    ACTUAL_VERSION="$("$1" --version 2>/dev/null | head -n 1 | tr -d "[:space:]")"',
    '    [ "$ACTUAL_VERSION" = "$PACKAGE_REQUESTED_VERSION" ]',
    "  }",
    '  if has_cmd corepack; then',
    "    corepack enable || true",
    '    log_tooling "Activating $PACKAGE_SPEC through Corepack"',
    '    if corepack prepare "$PACKAGE_SPEC" --activate; then',
    '      PACKAGE_MANAGER_BIN="$(package_manager_path)"',
    '      if [ -n "$PACKAGE_MANAGER_BIN" ] && package_manager_version_matches "$PACKAGE_MANAGER_BIN"; then',
    '        record_tooling "activate $PACKAGE_SPEC source=$PACKAGE_SOURCE"',
    "        return 0",
    "      fi",
    '      record_tooling "fallback $PACKAGE_SPEC reason=corepack-did-not-install-binary"',
    "    else",
    '      record_tooling "fallback $PACKAGE_SPEC reason=corepack-prepare-failed"',
    "    fi",
    "  fi",
    "  if has_cmd npm; then",
    '    log_tooling "Installing $PACKAGE_SPEC through npm"',
    '    if [ -n "$PACKAGE_NAME" ]; then',
    '      rm -f "$(npm bin -g 2>/dev/null)/$PACKAGE_NAME" 2>/dev/null || true',
