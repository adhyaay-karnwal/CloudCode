const RESET_EXEC_SCRIPT = [
  "#!/usr/bin/python3",
  "import os",
  "import signal",
  "import sys",
  "",
  "if len(sys.argv) < 2:",
  '    print("Usage: cloudcode-exec <command> [args...]", file=sys.stderr)',
  "    raise SystemExit(2)",
  "",
  "try:",
  "    signal.signal(signal.SIGCHLD, signal.SIG_DFL)",
  "except Exception:",
  "    pass",
  "",
  "os.execvpe(sys.argv[1], sys.argv[1:], os.environ)",
].join("\n")

const RESET_EXEC_FUNCTION = [
  'reset_exec() {',
  '  if [ -x /home/user/.cloudcode/bin/cloudcode-exec ] && [ -x /usr/bin/python3 ]; then',
  '    exec /home/user/.cloudcode/bin/cloudcode-exec "$@"',
  "  fi",
  '  exec "$@"',
  "}",
].join("\n")

