    'PACKAGE_MANAGER_SOURCE=""',
    'NODE_ENGINE_MIN=""',
    'NODE_ENGINE_SOURCE=""',
    'BUN_ENGINE_MIN=""',
    'BUN_ENGINE_SOURCE=""',
    "scan_package_manifests",
    'NODE_VERSION=""',
    'NODE_SOURCE=""',
