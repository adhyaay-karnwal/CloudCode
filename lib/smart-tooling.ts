    "trap - CHLD 2>/dev/null || true",
    'export MISE_YES=1',
    'export MISE_JOBS="${MISE_JOBS:-2}"',
    'export MISE_PYTHON_GITHUB_ATTESTATIONS=false',
    'export MISE_TRUSTED_CONFIG_PATHS="/home/user/repo"',
    'MANIFEST="/home/user/.cloudcode/tooling/manifest.txt"',
    "cd /home/user/repo",
    ': > "$MANIFEST"',
    'log_tooling() { echo "[smart-tooling] $*"; }',
    'record_tooling() { printf "%s\\n" "$*" >> "$MANIFEST"; }',
    'has_cmd() { command -v "$1" >/dev/null 2>&1; }',
    'first_line_version() { sed -n "1p" "$1" | tr -d "[:space:]" | sed "s/^v//"; }',
    'is_exact_version() { printf "%s" "$1" | grep -Eq "^v?[0-9]+([.][0-9]+){1,2}([-.+][A-Za-z0-9.]+)?$"; }',
    'version_key() { awk -F. \'{ printf "%03d%03d%03d\\n", $1, $2, $3 }\' <<< "$1"; }',
    'max_version() { if [ -z "$1" ]; then printf "%s\\n" "$2"; elif [ "$(version_key "$2")" -gt "$(version_key "$1")" ]; then printf "%s\\n" "$2"; else printf "%s\\n" "$1"; fi; }',
    "normalize_node_version() {",
    '  case "$1" in',
    '    *.*.*) printf "%s\\n" "$1" ;;',
    '    *.*) printf "%s.0\\n" "$1" ;;',
    '    *) printf "%s.0.0\\n" "$1" ;;',
    "  esac",
    "}",
    "node_engine_min_version() {",
    '  ENGINE_RANGE="$(printf "%s" "$1" | tr -d "\\n")"',
    '  CANDIDATE="$(printf "%s" "$ENGINE_RANGE" | sed -nE "s/.*>= *v?([0-9]+([.][0-9]+){0,2}).*/\\1/p" | head -n 1)"',
    '  if [ -z "$CANDIDATE" ]; then',
    '    CANDIDATE="$(printf "%s" "$ENGINE_RANGE" | sed -nE "s/.*[~^] *v?([0-9]+([.][0-9]+){0,2}).*/\\1/p" | head -n 1)"',
    "  fi",
    '  if [ -z "$CANDIDATE" ]; then',
    '    CANDIDATE="$(printf "%s" "$ENGINE_RANGE" | sed -nE "s/(^|.*[[:space:]])v?([0-9]+([.][0-9]+){0,2})([[:space:]].*|$)/\\2/p" | head -n 1)"',
    "  fi",
    '  if [ -n "$CANDIDATE" ]; then normalize_node_version "$CANDIDATE"; fi',
    "}",
    "find_manifest_files() {",
    "  find . -path './node_modules' -prune -o -path './.git' -prune -o -path './.next' -prune -o -path './dist' -prune -o -path './build' -prune -o -name package.json -print",
    "}",
    "find_repo_file() {",
    '  PATTERN="$1"',
    "  find . -path './node_modules' -prune -o -path './.git' -prune -o -path './.next' -prune -o -path './dist' -prune -o -path './build' -prune -o -name \"$PATTERN\" -print -quit",
    "}",
    "scan_package_manifests() {",
    '  PACKAGE_MANAGER=""',
    '  NODE_ENGINE_MIN=""',
    '  BUN_ENGINE_MIN=""',
    '  while IFS= read -r manifest; do',
    '    [ -f "$manifest" ] || continue',
    '    if [ -z "$PACKAGE_MANAGER" ]; then',
    '      MANAGER="$(json_value "$manifest" packageManager)"',
    '      if [ -n "$MANAGER" ]; then',
    '        PACKAGE_MANAGER="$MANAGER"',
    '        PACKAGE_MANAGER_SOURCE="$manifest packageManager"',
    "      fi",
    "    fi",
    '    if [ -z "$PACKAGE_MANAGER" ]; then',
    '      VOLTA_PNPM="$(json_value "$manifest" volta.pnpm)"',
    '      VOLTA_YARN="$(json_value "$manifest" volta.yarn)"',
    '      VOLTA_NPM="$(json_value "$manifest" volta.npm)"',
    '      if [ -n "$VOLTA_PNPM" ]; then PACKAGE_MANAGER="pnpm@$VOLTA_PNPM"; PACKAGE_MANAGER_SOURCE="$manifest volta.pnpm"; fi',
    '      if [ -z "$PACKAGE_MANAGER" ] && [ -n "$VOLTA_YARN" ]; then PACKAGE_MANAGER="yarn@$VOLTA_YARN"; PACKAGE_MANAGER_SOURCE="$manifest volta.yarn"; fi',
    '      if [ -z "$PACKAGE_MANAGER" ] && [ -n "$VOLTA_NPM" ]; then PACKAGE_MANAGER="npm@$VOLTA_NPM"; PACKAGE_MANAGER_SOURCE="$manifest volta.npm"; fi',
    "    fi",
    '    MANIFEST_NODE="$(json_value "$manifest" volta.node | sed "s/^v//")"',
    '    if [ -z "$MANIFEST_NODE" ]; then',
    '      NODE_ENGINE="$(json_value "$manifest" engines.node | sed "s/^v//")"',
    '      if [ -n "$NODE_ENGINE" ]; then',
    '        if is_exact_version "$NODE_ENGINE"; then MANIFEST_NODE="$NODE_ENGINE"; else MANIFEST_NODE="$(node_engine_min_version "$NODE_ENGINE")"; fi',
    "      fi",
    "    fi",
    '    if [ -n "$MANIFEST_NODE" ]; then',
    '      NODE_ENGINE_MIN="$(max_version "$NODE_ENGINE_MIN" "$MANIFEST_NODE")"',
    '      NODE_ENGINE_SOURCE="$manifest"',
    "    fi",
    '    MANIFEST_BUN=""',
    '    BUN_ENGINE="$(json_value "$manifest" engines.bun | sed "s/^v//")"',
    '    if [ -n "$BUN_ENGINE" ]; then',
    '      if is_exact_version "$BUN_ENGINE"; then MANIFEST_BUN="$BUN_ENGINE"; else MANIFEST_BUN="$(node_engine_min_version "$BUN_ENGINE")"; fi',
    '      if [ -n "$MANIFEST_BUN" ]; then BUN_ENGINE_MIN="$(max_version "$BUN_ENGINE_MIN" "$MANIFEST_BUN")"; BUN_ENGINE_SOURCE="$manifest engines.bun"; fi',
    "    fi",
    '  done < <(find_manifest_files)',
    "}",
    "write_json_reader() {",
    "  cat > /tmp/cloudcode-json-value.py <<'PY'",
    "import json",
    "import sys",
    "try:",
    "    with open(sys.argv[1], encoding=\"utf-8\") as f:",
    "        value = json.load(f)",
    "    for key in sys.argv[2].split(\".\"):",
    "        value = value[key]",
    "    if isinstance(value, (str, int, float)):",
    "        print(value)",
    "except Exception:",
    "    pass",
    "PY",
    "}",
    'json_value() { [ -f "$1" ] && has_cmd python3 && python3 /tmp/cloudcode-json-value.py "$1" "$2" 2>/dev/null || true; }',
    "ensure_mise() {",
    "  if ! has_cmd mise && [ ! -x /home/user/.local/bin/mise ]; then",
    '    log_tooling "Installing mise bootstrap"',
    "    curl https://mise.run | sh",
    "  fi",
    '  export PATH="/home/user/.local/bin:/home/user/.local/share/mise/shims:$PATH"',
    "  mkdir -p /home/user/.config/mise",
    '  export MISE_PYTHON_GITHUB_ATTESTATIONS=false',
    "  mise settings set python.github_attestations false || true",
