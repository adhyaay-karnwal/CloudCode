function commandWrapperScript(command: string) {
  const label = command[0].toUpperCase() + command.slice(1)
  return [
    "#!/usr/bin/env bash",
    "trap - CHLD 2>/dev/null || true",
    RESET_EXEC_FUNCTION,
    'SELF_DIR="/home/user/.cloudcode/bin"',
    'SEARCH_PATH="$(printf "%s" "$PATH" | tr ":" "\\n" | grep -vx "$SELF_DIR" | paste -sd: -)"',
    "exec_candidate() {",
    '  candidate_path="$1"',
    "  shift",
    `  if [ ${JSON.stringify(command)} = "node" ] && "$candidate_path" --experimental-strip-types -e "" >/dev/null 2>&1; then`,
    '    reset_exec "$candidate_path" --experimental-strip-types "$@"',
    "  fi",
    '  reset_exec "$candidate_path" "$@"',
    "}",
    "if command -v mise >/dev/null 2>&1; then",
    "  mise_node_root=\"$(mise where node 2>/dev/null || true)\"",
    '  if [ -n "$mise_node_root" ]; then',
    `    mise_candidate="$mise_node_root/bin/${command}"`,
    '    if [ -x "$mise_candidate" ]; then',
    '      exec_candidate "$mise_candidate" "$@"',
    "    fi",
    "  fi",
    `  for mise_candidate in /home/user/.local/share/mise/installs/node/*/bin/${command}; do`,
    '    if [ -x "$mise_candidate" ]; then',
    '      exec_candidate "$mise_candidate" "$@"',
    "    fi",
    "  done",
    "fi",
    `candidate="$(PATH="$SEARCH_PATH" command -v ${command} 2>/dev/null || true)"`,
    'if [ -n "$candidate" ] && [ -x "$candidate" ]; then',
    '  exec_candidate "$candidate" "$@"',
    "fi",
    `echo "${label} is not installed." >&2`,
    "exit 127",
  ].join("\n")
}

